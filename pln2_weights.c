void free_memory_pln2(pln2_t *s) {
    free(s->clusterCenters[0]);
    free(s->clusterCenters);
    free(s->R[0][5]);
    free(s->R[0][4]);
    free(s->R[0][3]);
    free(s->R[0][2]);
    free(s->R[0][1]);
    free(s->R[0][0]);
    free(s->R[0]);
    free(s->R);
    free(s->C[0][1]);
    free(s->C[0][0]);
    free(s->C[0]);
    free(s->C);
    free(s->Et[0]);
    free(s->Et);
    free(s->W[0][1]);
    free(s->W[0][0]);
    free(s->W[0]);
    free(s->W);
    free(s->lambda);
    free(s->inputMeans);
    free(s->inputStd);
    free(s->distanceMeasure);
    free(s->Nv);
}

